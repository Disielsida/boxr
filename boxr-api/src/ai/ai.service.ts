@Injectable()
export class AiService {
  private readonly genAI: GoogleGenerativeAI;
  private readonly modelName: string;

  constructor(private readonly config: ConfigService) {
    this.genAI = new GoogleGenerativeAI(config.get<string>('GEMINI_API_KEY') ?? '');
    this.modelName = config.get<string>('AI_MODEL') ?? 'gemini-2.5-flash';
  }

  async chat(messages: ChatMessageDto[], role: Role): Promise<ChatResponseDto> {
    if (messages.length === 0) {
      throw new InternalServerErrorException('Пустой ответ от AI');
    }

    const model = this.genAI.getGenerativeModel({
      model: this.modelName,
      systemInstruction: SYSTEM_PROMPT(role),
    });

    // Gemini history: all messages except the last; Gemini requires history to start with 'user'
    const rawHistory = messages.slice(0, -1).map((m) => ({
      role: m.role === 'user' ? 'user' : 'model',
      parts: [{ text: m.content }],
    }));
    const firstUserIdx = rawHistory.findIndex((m) => m.role === 'user');
    const history = firstUserIdx >= 0 ? rawHistory.slice(firstUserIdx) : [];
    const lastMessage = messages[messages.length - 1];

    let text: string;
    try {
      const chat = model.startChat({ history });
      const result = await chat.sendMessage(lastMessage.content);
      text = result.response.text();
    } catch {
      throw new InternalServerErrorException('AI сервис временно недоступен');
    }

    if (!text) {
      throw new InternalServerErrorException('Пустой ответ от AI');
    }

    return { content: text };
  }
}
